    public void populateAllPatientsTable() {
        List<PawPatient> patients = ((HealthCareEnterprise) enterprise).getPatientDirectory().getPatients();
        DefaultTableModel dtm = (DefaultTableModel) tblAllPawPatients.getModel();
        dtm.setRowCount(0);
        for (PawPatient patient : patients) {
            Object[] row = new Object[6];

            row[0] = patient;
            row[1] = patient.getPatientFirstName() + " " + patient.getPatientLastName();
            row[2] = patient.getContactNumber();
            row[3] = patient.getAddress();
            row[4] = patient.isIsTreatmentFinished() ? "Treatment Complete" : "Treatment In Progress";
            row[5] = patient.getAppointmentDate();

            dtm.addRow(row);
        }
        
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(dtm);
        tblAllPawPatients.setRowSorter(sorter);
    }
