    private void treatment_descActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_treatment_descActionPerformed
        List<PawPatient> underTreatmentPatients = new ArrayList<>();
        List<PawPatient> treatedPatients = new ArrayList<>();
        DefaultPieDataset defaultPieDataset = new DefaultPieDataset();
        List<PawPatient> patients = ((HealthCareEnterprise) enterprise).getPatientDirectory().getPatients();
        for (PawPatient patient : patients) {
            if (patient.isIsTreatmentFinished()) {
                treatedPatients.add(patient);
            } else {
                underTreatmentPatients.add(patient);
            }
        }
        defaultPieDataset.setValue("Patient still under treatment", underTreatmentPatients.size());
        defaultPieDataset.setValue("Patient Treated Successfully", treatedPatients.size());
        JFreeChart chart = ChartFactory.createPieChart("Patient Status Pie Chart", defaultPieDataset, true, true, true);
        PiePlot piePlot =(PiePlot) chart.getPlot();
        ChartFrame frame = new ChartFrame("Patient Status Pie Chart", chart);
        frame.setVisible(true);
        frame.setSize(500,500);
    }//GEN-LAST:event_treatment_descActionPerformed


